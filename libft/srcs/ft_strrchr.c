#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int i;

	i = 0;
	while ((char)s[i])
		i++;
	while (i > 0 && s[i] != c)
		i--;
	return (s[i] == c ? (char*)&s[i] : (char *)&s[0]);
}
